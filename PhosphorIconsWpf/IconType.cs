namespace PhosphorIconsWpf;

/// <summary>
/// Represents the different visual styles available for Phosphor icons.
/// </summary>
public enum IconType
{
    /// <summary>
    /// Thin style with the thinnest strokes for minimal designs.
    /// </summary>
    thin,
    
    /// <summary>
    /// Light style with thinner strokes for a delicate appearance.
    /// </summary>
    light,
    
    /// <summary>
    /// Regular style with standard stroke width (default style).
    /// </summary>
    regular,
    
    /// <summary>
    /// Bold style with thicker strokes for enhanced visibility.
    /// </summary>
    bold,
    
    /// <summary>
    /// Filled style with solid shapes instead of outlines.
    /// </summary>
    fill
}